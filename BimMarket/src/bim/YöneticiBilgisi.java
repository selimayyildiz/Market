package bim;

public class YöneticiBilgisi {
	private String yöneticiKullaniciAdi;
	private String yöneticiSifre;
	private int yöneticiId;
	
    public String getYöneticiKullaniciAdi() {
        return yöneticiKullaniciAdi;
    }

    public void setYöneticiKullaniciAdi(String yöneticiKullaniciAdi) {
        this.yöneticiKullaniciAdi = yöneticiKullaniciAdi;
    }

    public String getYöneticiSifre() {
        return yöneticiSifre;
    }

    public void setYöneticiSifre(String yöneticiSifre) {
        this.yöneticiSifre = yöneticiSifre;
    }

    public int getYöneticiId() {
        return yöneticiId;
    }

    public void setYöneticiId(int yöneticiId) {
        this.yöneticiId = yöneticiId;
    }
}
